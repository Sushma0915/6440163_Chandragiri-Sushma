// Program 7: Type Casting Example
public class TypeCastingExample {
    public static void main(String[] args) {
        double d = 9.78;
        int i = (int) d;
        int x = 5;
        double y = x;
        System.out.println("Double to Int: " + i);
        System.out.println("Int to Double: " + y);
    }
}